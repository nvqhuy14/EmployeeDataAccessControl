CREATE OR REPLACE TRIGGER encrypt_luong_phucap_trigger
BEFORE UPDATE ON NHANVIEN
FOR EACH ROW
DECLARE
    pragma autonomous_transaction;
    v_key RAW(50) := NULL; -- Encryption key
    v_iv RAW(50) := NULL; -- Initialization vector
    v_encrypted_luong RAW(4000);
    v_encrypted_phucap RAW(4000);
BEGIN
    -- Retrieve the encryption key and initialization vector from DANGNHAP table
    SELECT SALT, MATKHAU INTO v_iv, v_key
    FROM DANGNHAP
    WHERE MANV = :NEW.MANV;
    
        IF :OLD.LUONG IS NULL THEN
            v_encrypted_luong := DBMS_CRYPTO.ENCRYPT(
                src => UTL_I18N.STRING_TO_RAW(:NEW.LUONG, 'AL32UTF8'),
                typ => DBMS_CRYPTO.ENCRYPT_AES128 + DBMS_CRYPTO.CHAIN_CBC + DBMS_CRYPTO.PAD_PKCS5,
                key => v_key,
                iv => v_iv
            );
            :NEW.LUONG := v_encrypted_luong;
        END IF;
        
        IF :OLD.PHUCAP IS NULL THEN
