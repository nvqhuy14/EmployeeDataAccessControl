CREATE OR REPLACE PROCEDURE create_user(
    username_ IN VARCHAR2,
    password_ IN VARCHAR2
)
AS
    user_exists INTEGER;
    vaitro_ VARCHAR2(5);
BEGIN
    SELECT COUNT(*) INTO user_exists FROM all_users WHERE username = UPPER(username_);
    SELECT VAITRO INTO vaitro_ FROM NHANVIEN WHERE UPPER(MANV) = UPPER(username_);
    IF user_exists != 0 THEN
    -- existing user will be dropped
        EXECUTE IMMEDIATE 'DROP USER '||username_||' CASCADE';   
        
    END IF;
    -- create new user and grant neccessary privilieges to this user
    EXECUTE IMMEDIATE 'CREATE USER ' || username_ || ' IDENTIFIED BY ' || password_;
    EXECUTE IMMEDIATE 'GRANT CONNECT TO ' || username_;
    EXECUTE IMMEDIATE 'GRANT RESOURCE TO ' || username_;
    EXECUTE IMMEDIATE 'GRANT UNLIMITED TABLESPACE TO ' || username_;
    EXECUTE IMMEDIATE 'GRANT '||vaitro_||' TO '||username_;
    encrypt_EACH_NHANVIEN(username_);
    DBMS_OUTPUT.PUT_LINE('User ' || username_ || ' created successfully.');
END;
